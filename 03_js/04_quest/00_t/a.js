function x(n) {
	// 종료 조건: n이 0 또는 1일 때
	if (n == 0 || n == 1) {
	  return 1;
	}
	
	// 재귀 호출: n을 낮춰가면서 자기 자신을 호출하여 작은 팩토리얼 값을 얻음
	return n * x(n - 1);
  }
  
  // 팩토리얼 계산을 테스트해봅니다.

  var result = x(3); // 3! = 3 * 2 * 1 = 6

  console.log(result); 