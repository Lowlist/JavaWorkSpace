/**
 * Naming Conventions
 * 
 * 변수 이름 지을때
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용 할 수 있다.
 * 2) 특수기호는 언더스코어(바)와 달러를 사용 할 수 있다.
 * 3) 숫자로 이름을 시작 할수 없다
 *    1Name, 2Hello etc...
 * 4) 키워드는 변수명으로 사용 할 수 없다.
 *    var const = 'var'; etc...
 */

/**
 * Naming Conventions 2
 * 
 * 1) camelCase - 대부분의 언어에서 많이 사용
 *      단어와 단어가 나뉘어질때 대문자를 사용하여 표기
 * 2) snake_case - 파이썬같은 언어에서 사용
 *      단어와 단어가 나뉘어질때 _(언더스코어(바))를 사용하여 표기
 * 3) PascalCase - C# 마이크로소프트 계열의 언어에서 사용함
 *      앞 단어도 대문자를 사용하여 표기
*/
